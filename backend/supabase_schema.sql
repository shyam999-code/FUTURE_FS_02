-- ==========================================
-- 5. Create the `support_messages` table
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS public.support_messages (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    user_id UUID REFERENCES auth.users(id),
    email TEXT,
    subject TEXT NOT NULL,
    message TEXT NOT NULL,
    status TEXT DEFAULT 'open',
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 6. Enable Row Level Security (RLS)
-- ------------------------------------------
ALTER TABLE public.support_messages ENABLE ROW LEVEL SECURITY;

-- 7. Create RLS Policies for support_messages
-- ------------------------------------------

-- Allow INSERT: Anyone can send a message (e.g. from the website contact form)
CREATE POLICY "Allow anyone to insert messages" 
ON public.support_messages 
FOR INSERT 
WITH CHECK (true);

-- Allow ALL: Only admins can view and manage messages
-- We check for the 'role' field inside user_metadata
CREATE POLICY "Allow admins to manage all messages" 
ON public.support_messages 
FOR ALL
TO authenticated
USING ( (auth.jwt() -> 'user_metadata' ->> 'role') = 'admin' );

