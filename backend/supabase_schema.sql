-- 1. Create the `leads` table
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS public.leads (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- Primary key, auto-increment
    name TEXT NOT NULL CHECK (char_length(trim(name)) > 0), -- Required, cannot be empty
    email TEXT NOT NULL CHECK (char_length(trim(email)) > 0), -- Required, cannot be empty
    source TEXT,
    status TEXT DEFAULT 'new' CHECK (status IN ('new', 'contacted', 'converted')),
    notes TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL -- Default to now()
);

-- 2. Enable Row Level Security (RLS)
-- ------------------------------------------
-- By default, this restricts all access unless a policy grants it.
ALTER TABLE public.leads ENABLE ROW LEVEL SECURITY;

-- 3. Create RLS Policies for Authenticated Users Only
-- ------------------------------------------

-- Allow SELECT: Authenticated users can read all leads
CREATE POLICY "Allow SELECT only for authenticated users" 
ON public.leads 
FOR SELECT 
TO authenticated 
USING (true);

-- Allow INSERT: Authenticated users can insert new leads
CREATE POLICY "Allow INSERT only for authenticated users" 
ON public.leads 
FOR INSERT 
TO authenticated 
WITH CHECK (true);

-- Allow UPDATE: Authenticated users can update any lead
CREATE POLICY "Allow UPDATE only for authenticated users" 
ON public.leads 
FOR UPDATE 
TO authenticated 
USING (true);

-- Allow DELETE: Authenticated users can delete any lead
CREATE POLICY "Allow DELETE only for authenticated users" 
ON public.leads 
FOR DELETE 
TO authenticated 
USING (true);

